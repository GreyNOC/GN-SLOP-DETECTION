def test_summarize_ignores_non_pqc_findings() -> None:
    assert summarize_pq_readiness([])["status"] == "no_crypto_detected"
    eval_finding = Finding(
        rule_id="py.eval-on-input",
        title="eval",
        description="",
        severity=Severity.HIGH,
        confidence=Confidence.HIGH,
        category="eval",
        file_path="src/danger.py",
        line_start=1,
        line_end=1,
        snippet="eval(x)",
    )
    pq = summarize_pq_readiness([eval_finding])
    assert pq["status"] == "no_crypto_detected"
    assert pq["families"] == {}


