def test_flags_rsa_keygen_python(tmp_path: Path) -> None:
    _write(
        tmp_path,
        "src/keys.py",
        "from cryptography.hazmat.primitives.asymmetric import rsa\n"
        "key = rsa.generate_private_key(public_exponent=65537, key_size=2048)\n",
    )
    assert "pqc.rsa-in-use" in _rule_ids(_scan(tmp_path))


def test_flags_rsa_keygen_java_and_openssl(tmp_path: Path) -> None:
    _write(
        tmp_path,
        "src/Keys.java",
        'KeyPairGenerator kpg = KeyPairGenerator.getInstance("RSA");\n',
    )
    _write(tmp_path, "scripts/gen.sh", "openssl genrsa -out server.key 2048\n")
    result = _scan(tmp_path)
    rsa_findings = [f for f in result.findings if f.rule_id == "pqc.rsa-in-use"]
    assert {f.file_path for f in rsa_findings} == {"src/Keys.java", "scripts/gen.sh"}


